pub enum Instruction {
    ADC,
    AND,
    ASL,
    BCC,
    BCS,
    BEQ,
    BIT,
    BMI,
    BNE,
    BPL,
    BRK,
    BVC,
    BVS,
    CLC,
    CLD,
    CLI,
    CLV,
    CMP,
    CPX,
    CPY,
    DEC,
    DEX,
    DEY,
    EOR,
    INC,
    INX,
    INY,
    JMP,
    JSR,
    LDA,
    LDX,
    LDY,
    LSR,
    NOP,
    ORA,
    PHA,
    PHP,
    PLA,
    PLP,
    ROL,
    ROR,
    RTI,
    RTS,
    SBC,
    SEC,
    SED,
    SEI,
    STA,
    STX,
    STY,
    TAX,
    TAY,
    TSX,
    TXA,
    TXS,
    TYA,
    // the following aren't real instructions
    // they're pseudo instructions used to
    // implement interrupt logic
    RST,
    IRQ,
    NMI,
    // unofficial instructions
    ANC,
    ARR,
    ASR,
    DCP,
    ISC,
    JAM,
    LAS,
    LAX,
    RLA,
    RRA,
    SAX,
    SBX,
    SHA,
    SHS,
    SHX,
    SHY,
    SLO,
    SRE,
    XXA,
}

#[derive(Copy, Clone, Debug, Eq, PartialEq)]
pub enum Mode {
    Abs,
    AbsX,
    AbsY,
    A,
    Imm,
    Imp,
    AbsInd,
    IndX,
    IndY,
    Rel,
    Zp,
    Zpx,
    Zpy,
    // the following aren't real addressing modes of the 6502
    // they are used internally to make implementation more uniform
    Status,
    SP,
    X,
    Y,
}
